	void send_comando (int cs, char c)
{
	GPIOD->ODR=0; 
	if(cs==1){GPIOD->ODR|=(1UL<<8);}
	else if(cs==2){GPIOD->ODR|=(1UL<<9);}
	GPIOD->ODR|=c;
	GPIOD->ODR|=(1UL<<11);
	for(int g=0;g<10000;g++);
	GPIOD->ODR&=~(1UL<<11);
	GPIOD->ODR=0;
}

  


//Enviar los datos que s equieren mostrar en la gráfica

void send_dato (int cs, char b)
{
	GPIOD->ODR|=1UL<<10; //Configurar despues, muestra lo de la lcd
	if(cs==1){GPIOD->ODR|=(1UL<<8);}
	else if(cs==2){GPIOD->ODR|=(1UL<<9);}
	GPIOD->ODR|=b;
	GPIOD->ODR|=(1UL<<11);
	GPIOD->ODR&=~(1UL<<11);
	GPIOD->ODR=0;
}
