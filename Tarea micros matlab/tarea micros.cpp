//Configuraciones para encender la lcd y que se vea bien :3
char disp_on=0x3F;
char disp_off=0x3E;
char starL=0xC0;
char setX=0xB8;
char setY=0x40;

//Se envían los datos para configurar la lcd gráfica
	
	void configlcd (int cs, char c)
{
	GPIOC->ODR=0; 
	if(cs==1){GPIOC->ODR|=(1UL<<8);}
	else if(cs==2){GPIOC->ODR|=(1UL<<9);}
	GPIOC->ODR|=c;
	GPIOC->ODR|=(1UL<<11);
	for(int g=0;g<10000;g++);
	GPIOC->ODR&=~(1UL<<11);
	GPIOC->ODR=0;
}

//Enviar los datos que se quieren mostrar en la gráfica

void datoslcd (int cs, char b)
{
	GPIOC->ODR|=1UL<<10; //Configurar despues, muestra lo de la lcd
	if(cs==1){GPIOC->ODR|=(1UL<<8);} 
	else if(cs==2){GPIOC->ODR|=(1UL<<9);}
	GPIOC->ODR|=b;
	GPIOC->ODR|=(1UL<<11);
	GPIOC->ODR&=~(1UL<<11);
	GPIOC->ODR=0;
}

//Utiliza las 2 funciones datoslcd y configlcd, configlcd starL inicia la linea, y setx que va cambiando la página

void visu (int p)
{ //J corresponde al número de páginas que hay, y b va contando las columnas de cada página
	for(int j=0;j<8;j++)
		{
			configlcd(p,starL);
			configlcd(p,setX+j);
			for(int b=0;b<64;b++) //Va variando la posición que va mostrando en y
			{
				configlcd(p,setY+b);
				datoslcd(p,maux[j][b]);
			}
		}
}


